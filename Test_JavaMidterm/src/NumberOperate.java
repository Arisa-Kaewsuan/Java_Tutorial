
public class NumberOperate
{
	public int a,b ;
	
    public  NumberOperate(int a , int b)
    {
    	this.a = a;
    	this.b = b;
    }
    
    public NumberOperate()
	{
		
	}

	public static int add(int a , int b)
    {
    	int result;
    	result = a+b ;
    	return result;
    }
    
    public static int subtract(int a , int b)
    {
    	int subresult;
    	subresult = a-b ;
    	return subresult;
    }
    
    public static int multiply(int a , int b)
    {
    	int mulresult;
    	mulresult = a*b ;
    	return mulresult;
    }
    
    public static int divide(int a , int b)
    {
    	int divresult;
    	divresult = a+b ;
    	return divresult;
    }
 

}
